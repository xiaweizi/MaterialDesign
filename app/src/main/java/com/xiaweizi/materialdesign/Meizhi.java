package com.xiaweizi.materialdesign;

public class Meizhi {

    private String name;

    private int imageId;

    public Meizhi(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}